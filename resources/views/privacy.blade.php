<h5> b) Activities in Rural Water Supply</h5>

            <p>In the past, drinking water supply in rural areas in Mizoram had been outside the Governments sphere of influence, the community managed Improved Village Spring Source (IVSS/ Tuikhur), private wells and Individual Rain Water Harvesting System have often been main traditional sources of rural drinking water, the first government installed rural water supply schemes were implemented in the 1950s as part of the Government’s policy to provide basic drinking water supply facilities to the rural population. Since then, the involvement of Government has increased with corresponding decrease in the role of communities in the rural water supply sector. The Government of India’s role in the rural drinking water supply sector started in 1972-1973 with the launch of Accelerated Rural Water Supply Programme (ARWSP) to assist the States for providing portable water to the rural population.</p>

           <p> The Rural Water Supply (RWS) sector has now entered the fourth generation with major emphasis on ensuring sustainability of water availability, adequacy, convenience, affordability and equity by adopting decentralized approach involving VWSC and the community. The approach and objectives is to ensure drinking water to all population on a sustainable basis and in the principles of decentralized, demand driven. Adoption of appropriate technology revival of traditional systems, conjunctive use of surface and ground water, conservation, rain water harvesting and recharging of drinking water sources have been given major emphasis in the new approach.</p>

            <p>During 1972-1986 the main objects of RWS was to provide adequate drinking water to rural community through a centralized PHED. The second generation, another programme was started by introducing Technology Mission (1986-1987) and later renamed RGNDWM (1991-1992). The third generation Sector Reform Project was launched during 1999-2000 and the principles was to involve Community in planning, implementation and management of RWS schemes later scaled up as Swajaldhara in 2002.</p>

            <p>In our Country, women generally manage domestic water and an essential ingredient of community participation is improve women’s involvement, since women are the principal beneficiaries of this programme and are the pivot around which the entire sustainability paradigm is evolved. The level of service should be linked to the issue of demand, commonly expressed through user willingness-to-pay for a particular level of service and their satisfaction. Coverage of a particular village should be indicated based on these criteria. However, the issue of equity and the basic minimum need concept should be kept in mind which designing the schemes. Based on these, consideration the ARWSP has been modified as National Rural Drinking Water Programme (NRDWP) for the 11th Plan period.</p>

