<h2>THE MIZORAM&nbsp; WATER&nbsp; SUPPLIES (CONTROL) RULES , 2006</h2>
<div class="flex-center position-ref full-height">

<p>
	
	&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;In exercise of the powers confered by section 18 of the Mizoram Water Supplies (Control) Act, 2004 (No.11 of 2004) the Governor of Mizoram is pleased to make the following rules namely:-<br />
	1.&nbsp;&nbsp; &nbsp;Short title, Extent and commencement :<br />
	&nbsp;&nbsp; &nbsp;1)&nbsp;&nbsp; &nbsp;These rules may be called THE MIZORAM WATER SUPPLIES (CONTROL) RULES, 2006.<br />
	&nbsp;&nbsp; &nbsp;2)&nbsp;&nbsp; &nbsp;It shall have the like extent as the Principal Act.<br />
	&nbsp;&nbsp; &nbsp;3)&nbsp;&nbsp; &nbsp;They shall come into force on such date as the State Government may, by notification in the Official Gazette, appoint.<br />
	2.&nbsp;&nbsp; &nbsp;Definitions : &nbsp;<br />
	1)&nbsp;&nbsp; &nbsp;In these rules, unless the context otherwise requires,-<br />
	&nbsp;&nbsp; &nbsp;a)&nbsp;&nbsp; &nbsp;&ldquo;Act&rdquo; means The Mizoram Water Supplies (Control) Act, 2004(No.11 of 2004);<br />
	&nbsp;&nbsp; &nbsp;b)&nbsp;&nbsp; &nbsp;&ldquo;consumer&rdquo; means any person or owner or occupier of any premises provided with House Water connection, Public Water Point or Hand Pump Tube Well by Public Health Engineering Department;<br />
	&nbsp;&nbsp; &nbsp;c)&nbsp;&nbsp; &nbsp;&ldquo;disconnection&rdquo; means stoppage of supply of water due to certain reason which requires payment of re-connection fee and other expenses if any;<br />
	&nbsp;&nbsp; &nbsp;d)&nbsp;&nbsp; &nbsp;&ldquo;casual water connection&rdquo; means the water connection for a short period not more than 1(one) week or 7(seven) days;<br />
	&nbsp;&nbsp; &nbsp;e)&nbsp;&nbsp; &nbsp;&ldquo;habitation&rdquo; means any place(s) where people are living permanently.<br />
	&nbsp;&nbsp; &nbsp;f)&nbsp;&nbsp; &nbsp;&ldquo;kilolitre&rdquo; means one thousand litres;<br />
	&nbsp;&nbsp; &nbsp;g)&nbsp;&nbsp; &nbsp;&ldquo;premises&rdquo; means any land or building;&nbsp;&nbsp; &nbsp;<br />
	&nbsp;&nbsp; &nbsp;h)&nbsp;&nbsp; &nbsp;&ldquo;reconnection&rdquo; means reconnecting the disconnected water supply on payment of reconnection fee;<br />
	&nbsp;&nbsp; &nbsp;i)&nbsp;&nbsp; &nbsp;&ldquo;suspension&rdquo; means stoppage of supply of water for a certain period as notified in the suspension order. No need for&nbsp; payment of reconnection fee, but required payment of expenses if any,<br />
	&nbsp;&nbsp; &nbsp;j)&nbsp;&nbsp; &nbsp;&ldquo;temporary water connection&rdquo; means the water connection for a period more than 1(one) week but not exceeding 3(three) months;<br />
	&nbsp;&nbsp; &nbsp;k)&nbsp;&nbsp; &nbsp;&ldquo;Supply Tank&rdquo; means water tank(s) from where pipe(s) of house water connection(s) or Public Water Point(s) or Hydrant(s) are connected.<br />
	&nbsp;&nbsp; &nbsp;l)&nbsp;&nbsp; &nbsp;&ldquo;T-Cluster&rdquo; means point(s) on the water distribution pipe line(s) or end of water distribution pipe line(s) from where pipe(s) of house water connection(s) or Public&nbsp; Water Point(s) or hydrant(s) are connected.<br />
	&nbsp;&nbsp; &nbsp;m)&nbsp;&nbsp; &nbsp;&ldquo;Catchment area&rdquo; means the area of land draining into the source of water as notified by the Executive Engineer of the concerned Division as reserved catchment area.<br />
	&nbsp;&nbsp; &nbsp;m)&nbsp;&nbsp; &nbsp;&ldquo;Water Source&rdquo; means the source of water&nbsp; notified by the Executive Engineer of the concerned area as reserved water source.<br />
	(2)&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; &nbsp;All other words and expressions used herein and not defined shall have the meanings respectively assigned&nbsp; to them in the Act.<br />

